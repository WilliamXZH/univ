package com.group3.po;
/**
 * 用于接收购票时的座位分配结果
 * @author DLETC
 *
 */
public class SeatInfo {
	private Integer id;
	private Integer carriageId;
	private Integer carriageSerialNum;
	private String num;
	private Integer status;
	private String ids;//座位id序列
	
	public SeatInfo() {
		super();
	}
	public SeatInfo(Integer id, Integer carriageId, Integer carriageSerialNum, String num, Integer status, String ids) {
		super();
		this.id = id;
		this.carriageId = carriageId;
		this.carriageSerialNum = carriageSerialNum;
		this.num = num;
		this.status = status;
		this.ids = ids;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCarriageId() {
		return carriageId;
	}
	public void setCarriageId(Integer carriageId) {
		this.carriageId = carriageId;
	}
	public Integer getCarriageSerialNum() {
		return carriageSerialNum;
	}
	public void setCarriageSerialNum(Integer carriageSerialNum) {
		this.carriageSerialNum = carriageSerialNum;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getIds() {
		return ids;
	}
	public void setIds(String ids) {
		this.ids = ids;
	}
	@Override
	public String toString() {
		return "SeatInfo [id=" + id + ", carriageId=" + carriageId + ", carriageSerialNum=" + carriageSerialNum
				+ ", num=" + num + ", status=" + status + ", ids=" + ids + "]";
	}
	
	
}
