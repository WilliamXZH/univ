package com.group3.po;


public class TicketQueryCondition {

	private String departure;
	private String destination;
	private String date;

	private String ticketTypes;
	private String trainTypes;

	public TicketQueryCondition() {
		super();
	}

	public TicketQueryCondition(String departure, String destination, String date, String ticketTypes,
			String trainTypes) {
		super();
		this.departure = departure;
		this.destination = destination;
		this.date = date;
		this.ticketTypes = ticketTypes;
		this.trainTypes = trainTypes;
	}

	public String getDeparture() {
		return departure;
	}

	public void setDeparture(String departure) {
		this.departure = departure;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTicketTypes() {
		return ticketTypes;
	}

	public void setTicketTypes(String ticketTypes) {
		this.ticketTypes = ticketTypes;
	}

	public String getTrainTypes() {
		return trainTypes;
	}

	public void setTrainTypes(String trainTypes) {
		this.trainTypes = trainTypes;
	}

	@Override
	public String toString() {
		return "TicketQueryCondition [departure=" + departure + ", destination=" + destination + ", date=" + date
				+ ", ticketTypes=" + ticketTypes + ", trainTypes=" + trainTypes + "]";
	}

}
