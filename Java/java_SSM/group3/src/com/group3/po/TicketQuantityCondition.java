package com.group3.po;

public class TicketQuantityCondition {

	private Integer trainId;
	private Integer ticketTypeId;
	private String departure;
	private String destination;

	public TicketQuantityCondition() {
		super();
	}

	public TicketQuantityCondition(String departure, String destination) {
		super();
		this.departure = departure;
		this.destination = destination;
	}

	public TicketQuantityCondition(Integer trainId, String departure, String destination) {
		super();
		this.trainId = trainId;
		this.departure = departure;
		this.destination = destination;
	}

	public TicketQuantityCondition(Integer trainId, Integer ticketTypeId, String departure, String destination) {
		super();
		this.trainId = trainId;
		this.ticketTypeId = ticketTypeId;
		this.departure = departure;
		this.destination = destination;
	}

	public Integer getTrainId() {
		return trainId;
	}

	public void setTrainId(Integer trainId) {
		this.trainId = trainId;
	}

	public Integer getTicketTypeId() {
		return ticketTypeId;
	}

	public void setTicketTypeId(Integer ticketTypeId) {
		this.ticketTypeId = ticketTypeId;
	}

	public String getdeparture() {
		return departure;
	}

	public void setdeparture(String departure) {
		this.departure = departure;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	@Override
	public String toString() {
		return "TicketQuantityCondition [trainId=" + trainId + ", ticketTypeId=" + ticketTypeId + ", departure="
				+ departure + ", destination=" + destination + "]";
	}

}
